#[cfg(test)]
mod tests;

struct GodotGrpc;

#[gdextension]
unsafe impl ExtensionLibrary for GodotGrpc {
    // Start the Tokio runtime once Godot is fully initialized, tear it down
    // before shutdown. `MainLoop` is the fully-initialized stage (Godot 4.5+);
    // all classes are still available at the matching deinit.
    fn on_stage_init(stage: InitStage) {
        if stage == InitStage::MainLoop {
            runtime::start(2);
        }
    }

    fn on_stage_deinit(stage: InitStage) {
        if stage == InitStage::MainLoop {
            runtime::shutdown();
        }
    }

    // Per-frame main-thread pump: drain Tokio→Godot events and emit signals.
    // Runs after all Node `process()` methods (see bridge.rs for why this
    // replaces the spec's per-tree `_GrpcPump` Node).
    fn on_main_loop_frame() {
        bridge::drain();
    }
}
